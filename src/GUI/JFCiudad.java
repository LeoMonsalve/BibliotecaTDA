    private void btnListarCiudadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListarCiudadActionPerformed
        // TODO add your handling code here:
        try {
            int fila = 0;
            CiudadDAO ciudadDAO = new CiudadDAO();
            String[] titulos = {"ID CIUDAD", "NOMBRE CIUDAD", "ID REGION"};
            ArrayList<Ciudad> lista = ciudadDAO.listar();
            for(Ciudad ciudad : lista) {
                matrizCiudad[fila][0] = String.valueOf(ciudad.getId());
                matrizCiudad[fila][1] = ciudad.getNombre();
                matrizCiudad[fila][2] = String.valueOf(ciudad.getRegionId());
                fila++;
            }
            modelo = new DefaultTableModel(matrizCiudad, titulos);
            tblCiudad.setModel(modelo);
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnListarCiudadActionPerformed

    private void tblCiudadMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCiudadMouseClicked
        // TODO add your handling code here:
        int fila = tblCiudad.getSelectedRow();
        txtIdCiudad.setText(tblCiudad.getValueAt(fila, 0).toString());
        txtNombreCiudad.setText(tblCiudad.getValueAt(fila, 1).toString());
        txtIdRegion.setText(tblCiudad.getValueAt(fila, 2).toString());;
    }//GEN-LAST:event_tblCiudadMouseClicked

    public void limpiarControles() {
        txtIdRegion.setText("");
        txtNombreRegion.setText("");
        txtNombreCiudad.setText("");
        txtIdCiudad.setText("");
    }
