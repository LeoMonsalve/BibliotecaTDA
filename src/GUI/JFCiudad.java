    private void btnBuscarRegionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarRegionActionPerformed
        // TODO add your handling code here:
        try {
            RegionDAO regionDAO = new RegionDAO();
            Region region = regionDAO.buscar(txtNombreRegion.getText());
            if(region.getId() != 0) {
                txtIdRegion.setText(String.valueOf(region.getId()));
            } else {
                JOptionPane.showMessageDialog(null, "Region no encontrada");
                limpiarControles();
                txtIdRegion.requestFocus();
            }
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        
    }//GEN-LAST:event_btnBuscarRegionActionPerformed

    private void btnAgregarCiudadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarCiudadActionPerformed
        // TODO add your handling code here:
        try {
            Ciudad ciudad = new Ciudad();
            CiudadDAO ciudadDAO = new CiudadDAO();
            ciudad.setId(Integer.parseInt(txtIdCiudad.getText()));
            ciudad.setNombre(txtNombreCiudad.getText());
            ciudad.setRegionId(Integer.parseInt(txtIdRegion.getText()));
            ciudadDAO.insertar(ciudad);
            JOptionPane.showMessageDialog(null, "Ciudad ingresada");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }

        
    }//GEN-LAST:event_btnAgregarCiudadActionPerformed

    private void btnBuscarCiudadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarCiudadActionPerformed
        // TODO add your handling code here:
        try {
            RegionDAO regionDAO = new RegionDAO();
            
   
            CiudadDAO ciudadDAO = new CiudadDAO();
            Ciudad ciudad = ciudadDAO.buscar(Integer.parseInt(txtIdCiudad.getText()));
            if(ciudad.getId() != 0) {
                Region region = regionDAO.buscar(Integer.parseInt(txtIdRegion.getText()));
                txtNombreCiudad.setText(ciudad.getNombre());
                txtIdRegion.setText(String.valueOf(ciudad.getRegionId()));
                txtNombreRegion.setText(region.getNombre());
            } else {
                JOptionPane.showMessageDialog(null, "Ciudad no encontrada");
                limpiarControles();
                txtIdCiudad.requestFocus();
            }
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnBuscarCiudadActionPerformed

    private void btnEliminarCiudadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarCiudadActionPerformed
        // TODO add your handling code here:
        
        try {
            CiudadDAO ciudadDAO = new CiudadDAO();
            if(ciudadDAO.eliminar(Integer.parseInt(txtIdCiudad.getText())) != 0) {
                JOptionPane.showMessageDialog(null, "Ciudad Eliminada");
                limpiarControles();
                txtIdCiudad.requestFocus();
            } else {
                JOptionPane.showMessageDialog(null, "No se encontr√≥ ciudad para eliminar");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnEliminarCiudadActionPerformed

