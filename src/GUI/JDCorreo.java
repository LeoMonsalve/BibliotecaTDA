    private void btnAgregarCorreoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarCorreoActionPerformed
        // TODO add your handling code here:
        try {
            Correo correo = new Correo();
            CorreoDAO correoDAO = new CorreoDAO();
            
            correo.setId(Integer.parseInt(txtIdCorreo.getText()));
            correo.setCorreo(txtCorreo.getText());
            correo.setListaCorreo(Integer.parseInt(txtIdListaCorreo.getText()));
            correoDAO.insertar(correo);
            JOptionPane.showMessageDialog(null, "Autor ingresado");
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnAgregarCorreoActionPerformed

    private void btnBuscarCorreoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarCorreoActionPerformed
        // TODO add your handling code here:
        try {
            CorreoDAO correoDAO = new CorreoDAO();
            Correo correo = correoDAO.buscar(Integer.parseInt(txtIdCorreo.getText()));
            if(correo.getId() != 0) {
                txtCorreo.setText(correo.getCorreo());
                txtIdListaCorreo.setText(String.valueOf(correo.getListaCorreo()));
                
            } else {
                JOptionPane.showMessageDialog(null, "Correo no encontrado");
                limpiarControles();
                txtIdCorreo.requestFocus();
            }
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnBuscarCorreoActionPerformed

    private void btnEliminarCorreoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarCorreoActionPerformed
        // TODO add your handling code here:
        try {
            CorreoDAO correoDAO = new CorreoDAO();
            if(correoDAO.eliminar(Integer.parseInt(txtIdCorreo.getText())) != 0) {
                JOptionPane.showMessageDialog(null, "Correo Eliminado");
                limpiarControles();
                txtIdCorreo.requestFocus();
            } else {
                JOptionPane.showMessageDialog(null, "No se encontró correo para eliminar");
            }
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnEliminarCorreoActionPerformed

