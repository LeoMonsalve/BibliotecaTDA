    private void btnListarCorreoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListarCorreoActionPerformed
        // TODO add your handling code here:
        try {
            int fila = 0;
            CorreoDAO correoDAO = new CorreoDAO();
            String[] titulos = {"ID", "CORREO", "LISTA CORREO"};
            ArrayList<Correo> lista = correoDAO.listar();
            for(Correo correo : lista) {
                matrizCorreo[fila][0] = String.valueOf(correo.getId());
                matrizCorreo[fila][1] = correo.getCorreo();
                matrizCorreo[fila][2] = String.valueOf(correo.getListaCorreo());
                fila++;
            }
            modelo = new DefaultTableModel(matrizCorreo, titulos);
            tblCorreo.setModel(modelo);
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnListarCorreoActionPerformed

    public void limpiarControles() {
        txtIdCorreo.setText("");
        txtCorreo.setText("");
        txtIdListaCorreo.setText("");
    }
